a=28
b=10

#Arithmetic Operators

print("Arithmetic Operators:")
print("SUM:", a+b) #sum
print("SUB:", a-b) #sub
print("MULTIPICATION:", a*b) #multiply
print("DIVISION:", a/b) #divide
print("MODULUS:", a%b) #mod
print("FLOOR DIVISION:", a // b) # Floor Division
print("EXPONENTIATION:", a ** b) # Exponentiation


#Relational Operators

print("\nRelational Operators:")
print("SMALLER:", a<b)  #a is smaller then b
print("GREATER:", a>b)  #a is greater then b
print("SMALLER THEN EQUAL:", a<=b) #a is smaller then equal to b
print("GREATER THEN EQUAL:", a>=b) #a is greater then equal to b
print("EQUAL EQUAL:", a==b) #a is equal to equal to b
print("NOT EQUAL:", a!=b) #a is not equal to b