a= True
b= False
#Logical Operators

print("Logical Operators:")
print("AND:", a and b) #and
print("OR:", a or b)   #or
print("NOT A:", not a) #not a
print("NOT B:", not b) #not b

#Bitwise Operators

print("\nBitwise Operators:")
c = 10
d = 4
print("B_AND:", c & d)  #Bitwise AND 
print("B_OR:", c | d)   #Bitwise OR
print("B_NOT:", ~c)     #Bitwise NOT
print("B_XOR:", c ^ d)  #Bitwise XOR
print("B_RS:", c >> 2)  #Bitwise RIGHT SHIFT
print("B_LS:", c << 2)  #Bitwise LEFT SHIFT